# ── 加载模型 ──
print("加载GPT-2...")
tokenizer = GPT2Tokenizer.from_pretrained('gpt2')
tokenizer.pad_token = tokenizer.eos_token
model = GPT2LMHeadModel.from_pretrained('gpt2', output_hidden_states=True)
model.eval()
device = 'cuda' if torch.cuda.is_available() else 'cpu'
model = model.to(device)
print(f"设备: {device}")

# ── 收集数据 ──
print("\n收集激活 + logits...")
all_hidden = {i: [] for i in range(13)}
all_logits = []

with torch.no_grad():
    for idx, text in enumerate(TEXTS):
        tokens = tokenizer(text, return_tensors='pt', truncation=True, max_length=64)
        tokens = {k: v.to(device) for k, v in tokens.items()}
        outputs = model(**tokens)
        for layer_idx in range(13):
            all_hidden[layer_idx].append(outputs.hidden_states[layer_idx][0].cpu().numpy())
        all_logits.append(outputs.logits[0].cpu().numpy())
        if (idx+1) % 20 == 0:
            print(f"  {idx+1}/{len(TEXTS)}")

X_hidden = {i: np.vstack(all_hidden[i]) for i in range(13)}
X_logits  = np.vstack(all_logits)
X_prob    = torch.softmax(torch.tensor(X_logits), dim=-1).numpy()
N = X_hidden[0].shape[0]
print(f"总token数: {N}")

# ── 3个独立特征 ──
entropy    = -np.sum(X_prob * np.log(X_prob + 1e-10), axis=1)   # 不确定性
logit_skew = (X_logits.max(axis=1) - 2*np.median(X_logits, axis=1) + X_logits.min(axis=1))  # 分布偏度
logit_var  = X_logits.var(axis=1)                                 # 分布宽度

X_feat = np.column_stack([entropy, logit_skew, logit_var])
X_feat_s = StandardScaler().fit_transform(X_feat)

# 检查相关性
r_es, _ = spearmanr(entropy, logit_skew)
r_ev, _ = spearmanr(entropy, logit_var)
r_sv, _ = spearmanr(logit_skew, logit_var)
print(f"\n特征间相关性（越低越独立）:")
print(f"  entropy vs logit_skew: r={r_es:.3f}")
print(f"  entropy vs logit_var:  r={r_ev:.3f}")
print(f"  logit_skew vs logit_var: r={r_sv:.3f}")

# ── 辅助函数 ──
def get_pc1(X):
    return PCA(n_components=1).fit_transform(StandardScaler().fit_transform(X))[:, 0]

def bootstrap_r(y_true, y_pred, n=500, seed=42):
    rng = np.random.default_rng(seed)
    rs = []
    for _ in range(n):
        idx = rng.integers(0, len(y_true), len(y_true))
        r, _ = spearmanr(y_true[idx], y_pred[idx])
        rs.append(r)
    return np.array(rs)

