results_by_layer = []
for layer_idx in range(13):
    pc1 = get_pc1(X_hidden[layer_idx])

    # 单变量熵
    r_e, _ = spearmanr(pc1, entropy)

    # 3变量Ridge
    ridge = RidgeCV(alphas=[0.01, 0.1, 1.0, 10.0, 100.0], cv=5)
    ridge.fit(X_feat_s, pc1)
    pc1_pred = ridge.predict(X_feat_s)
    r_m, _ = spearmanr(pc1, pc1_pred)

    # Bootstrap
    boot = bootstrap_r(pc1, pc1_pred)
    ci_lo, ci_hi = np.percentile(boot, [2.5, 97.5])

    results_by_layer.append({
        'layer': layer_idx, 'r_entropy': r_e,
        'r_multi': r_m, 'ci_lo': ci_lo, 'ci_hi': ci_hi,
        'pc1': pc1, 'pc1_pred': pc1_pred, 'alpha': ridge.alpha_
    })

    flag = '★' if abs(r_m) > 0.7 else ('✓' if abs(r_m) > 0.5 else ' ')
    print(f"  L{layer_idx:<4} {r_e:>+10.4f} {r_m:>+10.4f} [{ci_lo:+.3f},{ci_hi:+.3f}]  {flag}")

# ── 目标层详细分析（最佳层）──
